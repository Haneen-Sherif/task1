void main(List<String> args) {
  int num1 = 25;
  int num2 = 5;
  add(num1, num2);
  sub(num1, num2);
  mul(num1, num2);
  div(num1, num2);
}

add(num1, num2) {
  print(num1 + num2);
}

sub(num1, num2) {
  print(num1 - num2);
}

mul(num1, num2) {
  print(num1 * num2);
}

div(num1, num2) {
  print(num1 / num2);
}
